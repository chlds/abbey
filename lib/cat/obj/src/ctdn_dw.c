signed(__cdecl ctdn_dw(signed(argt),signed(args),void(*argp))) {
auto signed r;
auto signed w = (7);
if(argt<(0x00)) AND(argt,0x00);
argt = (argt%(w));
r = cal_dw(args,argp);
if(!(0x01+(r))) return(r);
r = (r+(w+(0x01+(~argt))));
return(r%(w));
}
